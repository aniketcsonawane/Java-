    public static boolean chkpass(String pass,String cpass)
    {
        boolean flag = false;

        if (!pass.isEmpty()&&!cpass.isEmpty()) 
        {            
            if (pass.equals(cpass))
            {
                  flag = true;
            }
        }
        return flag;
    }
      //Email validation check
public static boolean getMail(String mail)
    {
        boolean flag = false;

        if (!mail.isEmpty()) 
        {            
            if (mail.contains(".") && mail.contains("@"))
            {
                  flag = true;
            }
        }
        return flag;
    }
  
