        // TODO add your handling code here:
        String name=jTextField1.getText();
        String abr=jTextField2.getText();
        String mob=jTextField3.getText();

        String mail=jTextField4.getText();

        String gn="";
        if ( jRadioButton1.isSelected())
        {
            gn=jRadioButton1.getText();
        }else if ( jRadioButton2.isSelected())
        {
            gn=jRadioButton2.getText();
        }
//             String user=jTextField5.getText();
           char ch[]=jPasswordField1.getPassword();
           String pass=String.valueOf(ch);
           
            char ch1[]=jPasswordField2.getPassword();
           String cpass=String.valueOf(ch1);
         
        if((name.isEmpty()))
        {
            JOptionPane.showMessageDialog(null, "Please enter name");
          

        }else if (abr.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please enter ABR");

        }else  if(!getGen(gen))
        {
            JOptionPane.showMessageDialog(null, "Please Select gender");
            

        }else  if(!getmob(mob))
        {
            JOptionPane.showMessageDialog(null, "Please enter Valid Moblie number ");
            

        }else if(!getMail(mail))
        {
            JOptionPane.showMessageDialog(null, "Please enter Valid Email Address");
           

        }else if ( user.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please enter UserName");

        }else if (!chkpass(pass,cpass))
        {
            JOptionPane.showMessageDialog(null, "Please enter Valid Password");

        }else{

            StaffDBupdator as=new StaffDBupdator();
            boolean flag=as.isUpdated(name, abr, gn, mob, mail, user, pass, cpass);

            if(flag)
            JOptionPane.showMessageDialog(null,"Staff Data Updated Successfully ");
            else
            JOptionPane.showMessageDialog(null,"Error occured Please try Again");

        }
